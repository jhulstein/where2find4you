function normalizeDemoPlace(place: Place): Place {
  if (place.source !== "seed") {
    return place;
  }

  return {
    ...place,
    isSponsored: false,
    sponsoredPriority: 0,
  };
}

export const samplePlaces = rawSamplePlaces.map(normalizeDemoPlace);

export const activePlaces = samplePlaces.filter((place) => place.isActive);
export const sponsoredPlaces = activePlaces
  .filter((place) => place.isSponsored)
  .sort((a, b) => b.sponsoredPriority - a.sponsoredPriority);
export const featuredPlaces = activePlaces.slice(0, 6);

export function getPlaceBySlug(slug: string) {
  return samplePlaces.find((place) => place.slug === slug || place.id === slug);
}

export function getPlacesByCategory(category: PlaceCategory | "all") {
  if (category === "all") {
    return activePlaces;
  }

  return activePlaces.filter((place) => place.category === category);
}

export const osloPlaces = activePlaces.filter((place) => place.city === "Oslo");

export const placeScoresByPlaceId = activePlaces.reduce<
  Record<string, { placeId: string; totalScore: number; summary: string }>
>((accumulator, place, index) => {
  accumulator[place.id] = {
    placeId: place.id,
    totalScore: Math.max(70, Math.min(96, Math.round((place.rating ?? 4) * 20) - index % 7)),
    summary: place.shortDescription,
  };
  return accumulator;
}, {});
